package co.phoenixlab.discord.api.entities;

public class Game {

    private String name;

    private int type;

    private String url;

    public Game() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public boolean isStreaming() {
        return type == 1;
    }
}
